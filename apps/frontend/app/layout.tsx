import type { Metadata } from 'next'
import localFont from 'next/font/local'
import './globals.css'

import AppProviders from './app.providers'
import { Container, CssBaseline } from '@mui/material'
import checkAuth from './actions/check-auth.action'
import logout from './actions/logout.action'
import Header from './components/header'

const geistSans = localFont({
  src: './fonts/GeistVF.woff',
  variable: '--font-geist-sans',
  weight: '100 900',
})
const geistMono = localFont({
  src: './fonts/GeistMonoVF.woff',
  variable: '--font-geist-mono',
  weight: '100 900',
})

export const metadata: Metadata = {
  title: 'Create Next App',
  description: 'Generated by create next app',
}

export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode
}>) {
  const isAuthenticated = await checkAuth()

  return (
    <html lang="en">
      <body
        className={`${geistSans.variable} ${geistMono.variable} antialiased`}
      >
        {/**
         * ensure that the styles are appended to the <head> and not rendering in the <body>
         **/}
        <AppProviders authenticated={isAuthenticated}>
          <CssBaseline />
          <Header logout={logout} />
          <Container className="mt-10">{children}</Container>
        </AppProviders>
      </body>
    </html>
  )
}
